# Exercise 6 - Sequence Slicing
# Question: Please complete the script so that it prints out a list slice containing items d , e , and f .
# letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]
# Expected output: 
# ['d', 'e', 'f'] 

def slice_DEF():
    letters = ["a", "b", "c", "d", "e", "f", "g", "h", "i", "j"]
    return letters[3:6]
print(slice_DEF())
